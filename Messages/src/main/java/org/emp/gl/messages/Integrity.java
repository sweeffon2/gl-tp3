package org.emp.gl.messages;

/**
 *
 * @author younes
 */
public abstract class Integrity extends Decor {
    
    public Integrity(IMessage message) {
        super(message);
    }
    public static String bytesToHex(byte[] bytes) {
        StringBuilder sb = new StringBuilder();
        for (byte b : bytes) {
            sb.append(String.format("%02x", b));
        }
        return sb.toString();
    }
}
